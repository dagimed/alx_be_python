# basic_operations.py

# Assign values to variables
num_a = 10
num_b = 5

# Perform operations
add = num_a + num_b
sub = num_a - num_b
mult = num_a * num_b

# Print results
print(f"Addition of {num_a} and {num_b} is {add}")
print(f"Subtraction of {num_a} and {num_b} is {sub}")
print(f"Multiplication of {num_a} and {num_b} is {mult}")
